#include "3-calc.h"
/**
 * op_add - Function adds integers
 * @a: integer parameter
 * @b: integer parameter
 * Return: sum of both parameters
 * Date: 22-03-2023
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function substracts integers
 * @a: int parameter
 * @b: int parameter
 * Return: difference between int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function multiplies two integers
 * @a: int parameter
 * @b: int parameter
 * Return: product of int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function divides two integers
 * @a: int parameter
 * @b: int parameter
 * Return: result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function returns modulus of int
 * @a: int parameter
 * @b: int parameter
 * Return:remainder of division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
